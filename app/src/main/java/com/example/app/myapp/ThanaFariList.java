package com.example.app.myapp;

public class ThanaFariList {

    public String oc;
    public String name;
    public String phone;



    public ThanaFariList (String oc, String name, String phone) {
        this.oc = oc;
        this.name = name;
        this.phone = phone;
    }


    public ThanaFariList() {}

    public String getOc() {
        return oc;
    }

    public void setOc(String oc) {
        this.oc = oc;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }






}
